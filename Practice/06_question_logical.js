// AND && if all values true then output is TRUE
console.log(true && true)
console.log(false && false)
console.log(true && false)
console.log(false && true)

// OR || if anyone value is true then output is TRUE
console.log(true || true)
console.log(false || false)
console.log(true || false)
console.log(false || true)

// NOT ! flip value 
console.log(!true)
console.log(!false)